package switchcase;

public class case4 {
	public static void main (String[]args) {
		char animal='f';
		switch (animal) {
		case 'a':System.out.println("ANT");break;
		case 'b':System.out.println("BUFELLOW");break;
		case 'c':System.out.println("CAT");break;
		case 'd':System.out.println("DOG");break;
		case 'e':System.out.println("ELEPHANT");break;
		case 'f':System.out.println("FROG");break;
		case 'g':System.out.println("GERAFHI");break;
		case 'h':System.out.println("HORSE");break;
		case 't':System.out.println("TIGER");break;
		case 'z':System.out.println("ZEBRA");break;
		case 'k':System.out.println("KINGKONG");break;
		case 'l':System.out.println("LION");break;
		default:System.out.println("NONE");break;
		}
	}
}
