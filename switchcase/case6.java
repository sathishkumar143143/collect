package switchcase;

public class case6 {
	public static void main (String[]args) {
		String icecream= "vennila";
		switch (icecream) {
		case "vennila":System.out.println("vennila RATE = 70");break;
		case "jamai":System.out.println("jamai RATE = 120");break;
		case "chocobar":System.out.println("chocobar RATE = 180");break;
		case "darkchocolate":System.out.println("darkchocolate RATE = 125");break;
		case "belgium":System.out.println("belgium RATE = 50");break;
		case "whitechocolate":System.out.println("whitechocolate RATE = 100");break;
		
		default:System.out.println("NONE");break;
		}
	}

}
