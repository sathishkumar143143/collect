package Day4;

public class Method {
	public static void main (String[]args) {
		String a=args[0];
		String b=args[1];
		String c=args[2];
		String d=args[3];
		int e=Integer.parseInt(b);
		long f=Long.parseLong(c);
		float g=Float.parseFloat(d);
		System.out.println(a);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
	}
}
