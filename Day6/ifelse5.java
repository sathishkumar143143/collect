package Day6;

public class ifelse5 {
	public static void main(String[]args) {
		char a='B';
		if(a=='A') {
			System.out.println(a+"is APPLE");
		}
		
		else if (a=='B') {
			System.out.println(a+"is BALL");
		}
		else if (a=='C') {
			System.out.println(a+"is CAT");
		}
		else {
			System.out.println("nothing");
		}
	}
	

}
